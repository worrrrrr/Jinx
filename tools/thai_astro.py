    lines.append(f"  - *{NAVANGA_MEANINGS.get(navanga_star_num, 'มีกำลังงานดวงดาวคุมธาตุปัญญาญาณอย่างเป็นสัดส่วน')}*")
    lines.append("")

    # ── Ⅴ. SECTION IV: วันเกิดและฤกษ์ชัยมงคล ──
    lines.append("---")
    lines.append("## Ⅳ. วิเคราะห์ฤกษ์เกิดและฤกษ์ชัยมงคล (Weekly Lord & Auspicious Guide)")
    lines.append("")
    weekday_idx = dt.weekday()  
    converted_weekday = (weekday_idx + 1) % 7
    
    day_name, quality, activities = DAY_AUSPICIOUS.get(converted_weekday, ("วันเกิด", "ปานกลาง", "การทำการค้าและการเจรจาปัญญา"))
