    lines.append(f"- **สมผุสองศาลัคนากำเนิด:** {lagna['deg']:.2f}°")
    lines.append("")
    lines.append("### 🪐 คำพยากรณ์ลัคนาชะตากำเนิดเชิงลึก:")
    lines.append(_get_detailed_thai_lagna_reading(lagna["sign"]))
    lines.append("")

