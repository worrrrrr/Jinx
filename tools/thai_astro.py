                readings.append(f"  • **ดาว {g_num} ({g_name}) สถิตภพ มรณะ (วิกฤติ/ต่างประเทศ/มรดก):** ดาวเคราะห์ดวงนี้ประสบเคราะห์เปลี่ยนผ่าน แนะนำให้ทำงานเบื้องหลัง ติดต่อกับต่างแดน หรือศึกษาศาสตร์เร้นลับ พลังด้านลบจะกลับกลายเป็นผลประโยชน์จากมรดกหรือโอกาสแปลกใหม่")
