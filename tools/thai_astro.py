def _get_detailed_thai_house_reading(bhavas: Dict, planets_data: Dict, lagna_sign: int) -> List[str]:
    """วิเคราะห์พระเคราะห์ ๑๐ ดวงใน ๑๒ เรือนภพชะตาตามระบบตรรกะกรรมศาสตร์ไทยอย่างถี่ถ้วน"""
    readings = []
    
    lagna_lord_id = _get_sign_lord_idx(lagna_sign)
    lord_data = planets_data.get(lagna_lord_id)
    if lord_data:
        lord_house = (lord_data["sign"] - lagna_sign + 12) % 12 + 1
        b_info = THAI_BHAVAS[lord_house-1]
        readings.append(
            f"  • **ดาวประจำตัวหลักของคุณ (ดาว {lord_data['num']} {lord_data['name']}) โคจรไปสถิตในเรือนภพที่ {lord_house} ({b_info[1]}):** "
            f"บ่งบอกว่าวิถีชะตาชีวิต เจตจำนง และเป้าหมายสูงสุดทั้งหมดของคุณจะถูกดึงดูดไปผูกพันอยู่กับเรื่องของ **{b_info[2]}** เป็นแกนกลางชะตาหลักในการสร้างความมั่นคงพึ่งพา"
        )

    for h in range(1, 13):
        b = THAI_BHAVAS[h-1]
        grahas_in = bhavas.get(h, [])
        if not grahas_in:
            continue
            
        for g_id in grahas_in:
            if g_id == lagna_lord_id:
                continue
                
            p_data = planets_data[g_id]
            g_num = p_data["num"]
            g_name = p_data["name"]
            g_desc = p_data["desc"]
            
            if h == 1:
                readings.append(f"  • **ดาว {g_num} ({g_name}) สถิตภพ ตนุ (ตัวตน):** ส่งผลให้คุณมีอุปนิสัยและรูปลักษณ์ได้รับอิทธิพลจากพลังงานของดาวดวงนี้เด่นชัด มีจุดยืนและเกณฑ์อำนาจของพลัง **{g_desc}** แสดงออกให้สังคมสัมผัสได้")
