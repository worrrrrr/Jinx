def _parse_datetime(text: str) -> Optional[datetime.datetime]:
    text = text.strip()
    m = re.search(r"(\d{1,2})/(\d{1,2})/(\d{4})\s+(\d{1,2})[.:](\d{1,2})", text)
    if m:
        d, mo, y, h, mi = map(int, m.groups())
        return datetime.datetime(y, mo, d, h, mi)
    m = re.search(r"(\d{1,2})/(\d{1,2})/(\d{4})", text)
    if m:
        d, mo, y = map(int, m.groups())
        return datetime.datetime(y, mo, d, 12, 0)
    thai_months = {
        'ม.ค.':1,'ก.พ.':2,'มี.ค.':3,'เม.ย.':4,'พ.ค.':5,'มิ.ย.':6,
        'ก.ค.':7,'ส.ค.':8,'ก.ย.':9,'ต.ค.':10,'พ.ย.':11,'ธ.ค.':12,
        'มกรา':1,'กุมภา':2,'มีนา':3,'เมษา':4,'พฤษภา':5,'มิถุนา':6,
        'กรกฎา':7,'สิงหา':8,'กันยา':9,'ตุลา':10,'พฤศจิกา':11,'ธันวา':12,
    }
    for name, num in thai_months.items():
        if name in text:
            parts = text.split()
            try:
                d = int(parts[0])
                y = int(parts[2]) if len(parts) > 2 else 2024
                if y > 2500: y -= 543
                h, mi = 12, 0
                tm = re.search(r"(\d{1,2})[.:](\d{1,2})", text)
                if tm: h, mi = map(int, tm.groups())
                return datetime.datetime(y, num, d, h, mi)
            except (ValueError, IndexError):
                pass
    return None


def execute_birth_chart_reading(dt: datetime.datetime) -> Dict[str, Any]:
    """คำนวณและพยากรณ์ดวงกำเนิดไทยสิบดาวสมผุสจริง"""
    if not _SWE_AVAILABLE:
        return {
            "status": "fail",
            "message": "ไม่สามารถเรียกใช้งาน Swiss Ephemeris ในระบบปัจจุบันได้"
        }
        
    try:
        jd = _jd_from_dt(dt)
        swe.set_sid_mode(swe.SIDM_LAHIRI)
        
        # สมผุสตำแหน่งดาว ๑๐ ดวงตามพิกัดดาราศาสตร์สากลอ้างอิง Lahiri Ayanamsa
        planets_data = {}
        for swe_id, num, name, emoji, desc in THAI_PLANETS:
            flags = swe.FLG_SIDEREAL | swe.FLG_SPEED
            res = swe.calc_ut(jd, swe_id, flags)
            lon = res[0][0]
            sign = int(lon / 30)
            deg = lon - sign * 30
            planets_data[swe_id] = {
                "lon": lon, "sign": sign, "deg": deg,
                "num": num, "name": name, "emoji": emoji, "desc": desc
            }
            
        lagna = _get_lagna(jd)
        lagna_sign = lagna["sign"]
        
        bhavas = _bhavas_from_lagna(planets_data, lagna)
            
        report = _get_detailed_thai_astro_report(dt, planets_data, lagna, bhavas)
        
        return {
            "status": "success",
            "result": report,
            "direct_response": True  
        }
        
    except Exception as e:
        return {
            "status": "fail",
            "message": f"กระบวนการพยากรณ์กำเนิดไทยเกิดข้อผิดพลาดเทคนิค: {type(e).__name__} - {str(e)}"
        }


